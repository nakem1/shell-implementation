#include <unistd.h>
#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int		n;

	n = 0;
	if (!s)
		return ;
	while (s[n])
	{
		write(fd, &s[n], 1);
		n++;
	}
}
